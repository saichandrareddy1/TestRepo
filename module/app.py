class calculatorapp:

    def __init__(self, a, b) -> None:
        self.a = a
        self.b = b

    def add(self):
        """
        This is the addition 
        self.a is the value 1
        self.b is the value 2

        the function will return addition of two values
        """
        return self.a + self.b
    
    def subract(self):
        """
        This is the addition 
        self.a is the value 1
        self.b is the value 2

        the function will return addition of two values
        """
        return self.a - self.b